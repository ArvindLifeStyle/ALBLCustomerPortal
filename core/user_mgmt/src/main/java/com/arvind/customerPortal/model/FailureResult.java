package com.arvind.customerPortal.model;

public class FailureResult {

	private String ok;
	private Https http$;
	private String why;

	public String getOk() {
		return ok;
	}

	public void setOk(String ok) {
		this.ok = ok;
	}

	public Https getHttp$() {
		return http$;
	}

	public void setHttp$(Https http$) {
		this.http$ = http$;
	}

	public String getWhy() {
		return why;
	}

	public void setWhy(String why) {
		this.why = why;
	}

}
